    public void setLeaderboard(String[] names, int[] scores)
    {
        // Get the table model from the JTable
        DefaultTableModel tableModel = (DefaultTableModel) leaderboard.getModel();

        // Modify the data in the table model
        
        for(int i=0; i<names.length; i++)
        {
            tableModel.setValueAt(i+1, i, 0);
            tableModel.setValueAt(names[i], i, 1);
            tableModel.setValueAt(scores[i], i, 2);
        }
    }
    
